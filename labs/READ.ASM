DATA SEGMENT PARA PUBLIC 'DATA'
	READ_MESSAGE DB "Please input the text: ","$"
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA

PUBLIC READ_TEXT

	;in ds:dx is the needed addres for int21h with ah=10h
	READ_TEXT PROC NEAR
		PUSHALL

		MOV BX,DX;SAVE DX
		
		MOV AH,09H
		LEA DX,READ_MESSAGE
		INT 21H;DISPLAY MESSAGE
		
		MOV AH,0AH
		MOV DX,BX;RESTORE INITIAL DX
		INT 21H;READ THE TEXT
		
		POPALL
		RET
	READ_TEXT ENDP

CODE ENDS
END READ_TEXT