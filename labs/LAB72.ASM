DATA SEGMENT PARA PUBLIC 'DATA'
LETTER DB 'aLA bALA pORTOCadasdlkm$'
LG DW $-LETTER
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
START PROC NEAR
ASSUME CS:CODE,DS:DATA
MOV AX,DATA
MOV DS,AX
XOR SI,SI
MOV CX,LG
CEVA:
CMP LETTER[SI],'a'
JB NOSMALL
CMP LETTER[SI],'z'
JA NOSMALL
SUB LETTER[SI],20H
NOSMALL:
INC SI
LOOP CEVA
MOV AH,09H
LEA DX,LETTER
INT 21H
RET
START ENDP
CODE ENDS
END START