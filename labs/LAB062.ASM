DATA1 SEGMENT PARA PUBLIC 'DATA'
	SOURCE_STRING DB 'alabala'
	LEN DW $-SOURCE_STRING
DATA1 ENDS


DATA2 SEGMENT PARA PUBLIC 'DATA'
	DEST_STRING db 100 dup(?)
DATA2 ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE,DS:DATA1

	MAIN PROC FAR
		PUSH DS
		XOR AX,AX
		PUSH AX
		
		MOV AX,DATA1
		MOV DS,AX;init ds with DATA1
		
		MOV AX,DATA2
		MOV ES,AX;init es with DATA2
		
		LEA SI,SOURCE_STRING
		LEA DI,DEST_STRING
		
		MOV CX, LEN
		;REP MOVSB
		
		NEXT:
			MOV AL,DS:[SI]
			MOV ES:[DI],AL
			INC SI
			INC DI
			LOOP NEXT
	RET
	MAIN ENDP
CODE ENDS

END MAIN
