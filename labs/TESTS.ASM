DATA SEGMENT PARA PUBLIC 'DATA'
	ASDF DB 100 DUP(1)
DATA ENDS

DATA2 SEGMENT PARA PUBLIC 'DATA'
	ASDF1 DB 100 DUP(2)
	TABEL DB '0123456789ABCDEF'
DATA2 ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA
	MAIN PROC FAR
		PUSH DS
		XOR AX,AX
		PUSH AX;init program return
		MOV AX,DATA
		MOV DS,AX;init ds
		
		MOV CX,DATA2
		MOV ES,CX
		
		MOV AL,5
		MOV BX,OFFSET TABEL
		XLAT ES:TABEL
		
		MOV SI,3
		LEA DX,TABEL[SI]
		
		RET
	MAIN ENDP

CODE ENDS
END MAIN