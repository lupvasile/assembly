DATA SEGMENT PARA PUBLIC 'DATA'
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA

PUBLIC READ_AX

	READ_AX PROC NEAR

		PUSH BX
		PUSH CX
		
		AGAIN:  MOV AH,01H
				INT 21H;READ CHAR
				
				CMP AL,13
				JE FINISH
				
				CMP AL,' '
				JE FINISH
				
				SUB AL,'0';MAKE CHAR NUMBER
				
				XOR CH,CH
				MOV CL,AL;SAVE NUMBER
				
				MOV AX,10
				MUL BX;AX = AX*10
				ADD AX,CX
				MOV BX,AX
				JMP AGAIN
		
		FINISH: MOV AX,BX
	
		POP CX
		POP BX
		RET
	READ_AX ENDP	
CODE ENDS
END READ_AX