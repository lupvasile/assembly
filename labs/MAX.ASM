CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE
PUBLIC FIND_MAX

	;DS:BX SIRUL, DS:SI LUNGIMEA, DS:DI RETURNEZI MAXIMUL
	FIND_MAX PROC NEAR
		PUSHALL
		
		XOR AX,AX;INIT AX WITH 0, AX WILL STORE THE MAX VALUE
		MOV CX,[SI]
		
		NEXT: CMP [BX],AL
			  JNA NOT_ABOVE
			  MOV AL,[BX]
			  
			  NOT_ABOVE:
			  INC BX
			  LOOP NEXT
		
		MOV [DI],AL
		POPALL
		RET
	FIND_MAX ENDP

CODE ENDS
END FIND_MAX