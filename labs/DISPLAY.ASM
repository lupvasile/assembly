DATA SEGMENT PARA PUBLIC 'DATA'
	WRITE_MESSAGE_TEXT DB 10,13,"The text with swapped cases is: ","$"
	WRITE_MESSAGE_VOWEL DB 10,13,"The nr. of vowels is: ","$"
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:CODE, DS:DATA

EXTRN DISPLAY_AX:NEAR

PUBLIC DISPLAY_ALL

	;in DX-> TEXT, BX->NR VOWELS
	DISPLAY_ALL PROC NEAR
		PUSHALL
		
		MOV CX,DX;SAVE DX
		
		MOV AH,09H
		LEA DX,WRITE_MESSAGE_TEXT
		INT 21H;DISPLAY MESSAGE
		
		MOV DX,CX;RESTORE INITIAL DX
		INT 21H;DISPLAY TEXT

		MOV AH,09H
		LEA DX,WRITE_MESSAGE_VOWEL
		INT 21H;DISPLAY MESSAGE

		
		MOV AX,[BX]
		CALL DISPLAY_AX
		
		POPALL
		RET
	DISPLAY_ALL ENDP

CODE ENDS
END DISPLAY_ALL