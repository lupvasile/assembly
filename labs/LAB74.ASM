DATA SEGMENT PARA PUBLIC 'DATA'
MSG DB 'The average is: $'
ARR DB 1,2,3,4,5,6,7,8,9
LG DB $-ARR
NR DB 0
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
START PROC NEAR
ASSUME CS:CODE,DS:DATA
MOV AX,DATA
MOV DS,AX
MOV CL,LG
MOV SI,0
XOR AX,AX
AVERAGE:
	CMP ARR[SI],5
	JL NOGOOD
	CMP ARR[SI],10
	JA NOGOOD
	ADD AL,ARR[SI]
	INC NR
	NOGOOD:
	INC SI
LOOP AVERAGE
DIV NR
PUSH AX
MOV AH,09H
LEA DX,MSG
INT 21H
POP DX
ADD DX,30H
MOV AH,02H
INT 21H

RET
START ENDP
CODE ENDS
END START