;REZ = AL * NUM1 + (NUM2 * AL + BL)
DATA SEGMENT PARA PUBLIC 'DATA'
	REZ DW 0
	NUM1 DB 2
	NUM2 DB 3
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'

START PROC FAR
ASSUME CS:CODE, DS:DATA
	PUSH DS
	XOR AX,AX
	PUSH AX
	MOV AX,DATA
	MOV DS,AX

ENTRY:
	MOV AL, 03H
	MOV CL, AL ;save AL for later use
	MUL NUM1
	MOV REZ,AX ;REZ = AL*NUM1
	
	MOV BL, 1FH
	MOV BH, 0H ; make bx
	ADD REZ, BX
	
	MOV AL, CL
	MUL NUM2
	ADD REZ, AX 
	
	RET
START ENDP
CODE ENDS
END START