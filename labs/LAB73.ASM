DATA SEGMENT PARA PUBLIC 'DATA'
MSG DB 'The average is: $'
ARR DB 1,2,3,4,5,6,7,8,9
LG DB $-ARR
DATA ENDS

CODE SEGMENT PARA PUBLIC 'CODE'
START PROC NEAR
ASSUME CS:CODE,DS:DATA
MOV AX,DATA
MOV DS,AX
MOV CL,LG
MOV SI,0
XOR AX,AX
AVERAGE:
	ADD AL,ARR[SI]
	INC SI
LOOP AVERAGE
DIV LG
PUSH AX
MOV AH,09H
LEA DX,MSG
INT 21H
POP DX
ADD DX,30H
MOV AH,02H
INT 21H

RET
START ENDP
CODE ENDS
END START